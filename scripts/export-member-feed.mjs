import fs from 'node:fs/promises';
import path from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const yaml = require('js-yaml');

const reposDir = path.resolve('src/content/repos');
const outDir = path.resolve('functions/_generated');
const outFile = path.join(outDir, 'member-feed.js');

const files = (await fs.readdir(reposDir)).filter((file) => file.endsWith('.md') && !file.startsWith('_')).sort();

const items = [];
for (const file of files) {
  const raw = await fs.readFile(path.join(reposDir, file), 'utf8');
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) continue;

  const data = yaml.load(match[1]) || {};
  if (data.draft) continue;

  const slug = file.replace(/\.md$/, '');
  const guide = data.practitionerGuide || {};
  items.push({
    slug,
    url: `/repos/${slug}`,
    name: data.name,
    repoUrl: data.repoUrl,
    oneLiner: data.oneLiner,
    vertical: data.vertical || [],
    maturity: data.maturity || 'rising',
    repoStats: data.repoStats || null,
    signalSources: data.signalSources || [],
    useCases: data.useCases || [],
    workflowStepReplaced: data.workflowStepReplaced || '',
    timeOrCostSaved: data.timeOrCostSaved || '',
    localProblem: data.localProblem || '',
    localEvidence: data.localEvidence || '',
    usabilityRisk: data.usabilityRisk || '',
    suggestedAngle: data.suggestedAngle || '',
    paidToolReplaced: data.paidToolReplaced || '',
    practitionerGuide: {
      outcome: guide.outcome || '',
      prerequisites: guide.prerequisites || [],
      steps: guide.steps || [],
      expectedResult: guide.expectedResult || '',
      commonPitfalls: guide.commonPitfalls || [],
    },
    tags: data.tags || [],
    publishedAt: formatDate(data.publishedAt),
    week: data.week,
  });
}

items.sort((a, b) => String(b.publishedAt).localeCompare(String(a.publishedAt)) || a.name.localeCompare(b.name));

const feed = {
  title: 'RepoRadar VN',
  version: '0.3',
  generatedAt: new Date().toISOString(),
  count: items.length,
  items,
};

await fs.mkdir(outDir, { recursive: true });
await fs.writeFile(
  outFile,
  `// Generated by scripts/export-member-feed.mjs. Do not edit by hand.\nexport const memberFeed = ${JSON.stringify(feed, null, 2)};\n`,
);

console.log(`Exported ${items.length} member feed items to ${path.relative(process.cwd(), outFile)}`);

function formatDate(value) {
  if (!value) return '';
  if (value instanceof Date) return value.toISOString();
  const date = new Date(value);
  return Number.isNaN(date.getTime()) ? String(value) : date.toISOString();
}
